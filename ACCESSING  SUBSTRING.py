#Concept of Indexing
#Types of indexing
#positive indexing in this we go from left to right
a = "Hello"
print(a[0])
print(a[1])
print(a[2])
print(a[3])
print(a[4])

#negative indexing in this we go from right to left
a = "Hello"
print(a[-1])
print(a[-2])
print(a[-3])
print(a[-4])
print(a[-5])

#Slicing
b = "HELLO BUDDY"
print(b[0 : 5])
print(b[4: ])
print(b[ :3])
print(b[2:10:2])
print(b[::-1])#negative index slicing
print(b[-1:-6:-1])
